    private void button_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_loginActionPerformed
        // TODO add your handling code here:
        try {
            // TODO add your handling code here:
            conn = DatabaseUtilities.getConnection();
            String sql =  "select * from admin where username = ? and password = ?";
            pst = conn.prepareStatement(sql);
            pst.setString(1, textField_username.getText());
            pst.setString(2, passwordField_password.getText());
            rs = pst.executeQuery();
            
            if(rs.next()){
                JOptionPane.showMessageDialog(null, "Login success");
                dispose();
//                new ManipulationData(this).setVisible(true);
                new ManipulationData().setVisible(true);
            }else{
                JOptionPane.showMessageDialog(null, "Username or password is not correct");
            }
        } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_button_loginActionPerformed

